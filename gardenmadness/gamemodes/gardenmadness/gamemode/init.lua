function LoadBushes()
	if mapexamples[game.GetMap()] == nil then return end
	local allbushes = {}
	table.Add(allbushes, mapexamples[game.GetMap()])
	
	for k,v in pairs(gteams.GetPlayers(TEAM_FLOWERS)) do
		local randombush = table.Random(allbushes)
		v:SetPos(randombush)
		table.RemoveByValue(allbushes, randombush)
	end
	for k,v in pairs(allbushes) do
		local bush = ents.Create( "ent_gma_bush" )
		local randomnumber = math.random(1,3)
		if ( !IsValid( bush ) ) then return end
		bush:SetPos( v )
		bush:Spawn()
	end
end


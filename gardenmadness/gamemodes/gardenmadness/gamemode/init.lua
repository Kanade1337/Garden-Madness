net.Receive( "ChangeTeam", function( len, ply )
	local gotstr = net.ReadString()
	if gotstr != "1" and gotstr != "2"then return end
	local canjoinf = false
	local canjoing = false
	
	local flpl = #PF()
	local gpl = #PG()
	if flpl < gpl then
		canjoing = false
		canjoinf = true
	elseif flpl == gpl then
		canjoing = false
		canjoinf = false
	elseif flpl > gpl then
		canjoing = true
		canjoinf = false
		end
	if gotstr == "1" then
		// Player want to join Gardeners
		if ply.wantteam == TEAM_GARDEN then canjoing = false end
		if canjoing then
			ply:ColouredMsg(Color(220,220,0), "You will join team ", gteams.GetColor(TEAM_GARDEN), "GARDEN", Color(220,220,0), " in the next round.")
			ply.wantteam = TEAM_GARDEN
		else
			ply:ColouredMsg(Color(220,220,0), "You cannot change your team now.")
		end
	elseif gotstr == "2" then
		// Player want to join Flowers
		if ply.wantteam == TEAM_FLOWERS then canjoinf = false end
		if canjoinf then
			ply:ColouredMsg(Color(220,220,0), "You will join team ", gteams.GetColor(TEAM_GARDEN), "PLANTS", Color(220,220,0), " in the next round.")
			ply.wantteam = TEAM_FLOWERS
		else
			ply:ColouredMsg(Color(220,220,0), "You cannot change your team now.")
		end
	end
end)

